/**
 * @author zzhihua
 * @date 2022/3/10
 */
public class ListNode {
    
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
